def lambda_task_5_3(students):
    """
    Sort students by their grade (highest to lowest).
    Args:
        students: list of dictionaries [{"name": "Alice", "grade": 85}, {"name": "Bob", "grade": 92}, {"name": "Charlie", "grade": 78}]
    Returns:
        sorted list of students by grade (highest to lowest)
    """
    return sorted(students,key=lambda i: i.isdigit())
